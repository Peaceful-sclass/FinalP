package com.wv.root.model.dao;

public class mypara {
	private int myco;
	private int myteam;
	public mypara(int myco, int myteam) {
		super();
		this.myco = myco;
		this.myteam = myteam;
	}
	public int getMyco() {
		return myco;
	}
	public void setMyco(int myco) {
		this.myco = myco;
	}
	public int getMyteam() {
		return myteam;
	}
	public void setMyteam(int myteam) {
		this.myteam = myteam;
	}
	
	
}
